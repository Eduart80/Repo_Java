package Interview;

public class minimumValue {
    public static void main(String[] args) {
        //double min value
        System.out.println(Double.MIN_VALUE);// is positive
        System.out.println(Long.MIN_VALUE); // negative number
        System.out.println(Float.MIN_VALUE); // positive number


        System.out.println(Math.min(Double.MIN_VALUE, 0.0d)); // output zero




    }
}
