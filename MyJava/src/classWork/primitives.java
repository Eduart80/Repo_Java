package classWork;

public class primitives {
    public static void main(String[] args) {

         /*Primitive
         *byte, short, int,long,
         *float, double
         *char, boolean
         *
         *Non-primitives
         * class, string, array, interface
         *
          */
        byte holeNumber = 20;               // 8 bit -128 to 127
        short holeNumb = 220;               // 16 bit   -32,768 to 32,767
        int holeNumber1 = 300_000_000;      // 32 bit    -2,147,483,648 to 2,147,483,647
        long holeNumbe = 333_333_333L;      // 64 bit    -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        float decimal = 2069870.0098765F;       // 32 bit    storing 6 to 7 decimal digits
        double decimal2 = 200000985656986.5563621D;     // 64 bit    storing 15 decimal digits
        boolean  bool = true;               // true or false values
        char character = 'w';  // 16 bit

        System.out.println(holeNumber);
        System.out.println(holeNumb);
        System.out.println(holeNumber1);
        System.out.println(holeNumbe);
        System.out.println(decimal);
        System.out.println(decimal2);
        System.out.println(bool);
        System.out.println(character);

        



    }
}
